'''
Après avoir déclaré une variable annee, définir une suite d'instructions qui permettra de dire si la valeur de cette variable est bissextile.

A l'aide d'une boucle, déterminer les prochaines années bissextiles jusqu'en 2 500.
'''

annee = 0

# Permet de savoir si la variable 'annee' est bissextile
if annee%4 == 0:
        if annee%100 == 0:
            if annee%400 == 0:
                print(annee)
        else:
            print(annee)

# Détermine les prochaines années bissextiles jusqu'en 2500

for i in range(2021,2501):
    if i%4 == 0:
        if i%100 == 0:
            if i%400 == 0:
                print(i)
        else:
            print(i)
